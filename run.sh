#!/bin/bash

make clean && make

echo ""
echo "Size: 10000x10000 * 10000x10000"
./sparsematmult 10000 10000 10000 0.05 | tee 10000x10000*1000x1000_0.05.log
./sparsematmult 10000 10000 10000 0.10 | tee 10000x10000*1000x1000_0.10.log
./sparsematmult 10000 10000 10000 0.15 | tee 10000x10000*1000x1000_0.15.log
./sparsematmult 10000 10000 10000 0.20 | tee 10000x10000*1000x1000_0.20.log
echo ""

echo "Size: 20000x5300 * 5300x50000"
./sparsematmult 20000 5300 50000 0.05 | tee 20000x5300*5300x50000_0.05.log
./sparsematmult 20000 5300 50000 0.10 | tee 20000x5300*5300x50000_0.10.log
./sparsematmult 20000 5300 50000 0.15 | tee 20000x5300*5300x50000_0.15.log
./sparsematmult 20000 5300 50000 0.20 | tee 20000x5300*5300x50000_0.20.log
echo ""

echo "Size: 9000x35000 * 35000x5750"
./sparsematmult 9000 35000 5750 0.05 | tee 9000x35000*35000x5750_0.05.log
./sparsematmult 9000 35000 5750 0.10 | tee 9000x35000*35000x5750_0.10.log
./sparsematmult 9000 35000 5750 0.15 | tee 9000x35000*35000x5750_0.15.log
./sparsematmult 9000 35000 5750 0.20 | tee 9000x35000*35000x5750_0.20.log
echo ""